num1 = 10
num2 = 20

# 比较运算是有结果的, 结果是一个布尔值
print(num1 > num2)
print(num1 < num2)

print(num1 >= num2)
print(num1 <= num2)

num1 = 10
num2 = 10
print(num1 >= num2)
print(num1 <= num2)

print(num1 != num2)
print(num1 == num2)  # 不是赋值, 是判断多个值是否相等


num1 = '3'
num2 = '3'
print(num1 == num2)



