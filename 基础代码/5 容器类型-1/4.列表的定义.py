"""
python中有内置的几种数据结构: 列表、字典、元组、集合
    数据结构可以保存多个数据

    list定义的方式:
        data_list = []  代表创建了一个空列表
        data_list = list()  代表创建了一个空列表
"""

# 列表可以保存多个不同类型的数据
data_list = [1, 2, 3, 4, '123', 3.14, True, False]
print(data_list)

# 空列表的声明方式
data_list = []
print(data_list)

data_list = list()
print(data_list)


