"""
1.常量:
    固定不变的量
        100 3.14 '双双胖三斤'
2.变量:
    可以变化的量
"""


# 变量的声明
number = 1  # number就是一种变量
print(number)

number = 2
print(number)  # 这里的变量值打印和第一次不一样


num = 10
print(num)
num = 20
print(num)

# 常量和变量的计算

# 变量计算
num1 = 100
num2 = 200
print(num1 + num2)

# 常量计算
print(500 + 100)


# 在计算的过程中可以将计算结果赋值给一个新的变量
result_1 = 50 + 10
print(result_1)

result_2 = num1 + num2
print(result_2)




