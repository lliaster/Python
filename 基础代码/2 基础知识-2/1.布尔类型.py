# 布尔类型主要用于判断
# 布尔类型只有两个值: Ture / False

have_money = True  # 条件为真
print(have_money)

have_money = False  # 条件为假
print(have_money)


# 举例: 判断案例
if have_money:
    print('你真有钱')
else:
    print('你是真穷')
